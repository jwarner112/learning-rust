fn main() {
    // addition
    let sum = 5 + 10;
    // subtraction
    let difference = 95.5 - 4.3;
    // multiplication
    let product = 4 * 30;
    // division
    let quotient = 56.7 / 32.2;
    // remainder
    let remainder = 43 % 5;

    print!("\n");
    print!("In Rust, all standard numeric operations are supported.\n");
    print!("    Add:  5   + 10   = {}\n", sum);
    print!("    Sub: 95.5 -  4.3 = {}\n", difference);
    print!("    Mlt:  4   * 30   = {}\n", product);
    print!("    Div: 56.7 / 32.2 = {}\n", quotient);
    print!("    Mod: 43   %  5   = {}\n", remainder);
    print!("\n");
}
